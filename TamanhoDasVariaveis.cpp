#include <iostream>
using namespace std;
int main(){
    bool a;
    short b;
    int c;
    char d;
    float e;
    double f;
    cout << "Tamanho das variaveis na memoria do computador" << endl;
    cout << "bool --> " << sizeof(a) << endl;
    cout << "short --> " << sizeof(b) << endl;
    cout << "int --> " << sizeof(c) << endl;
    cout << "char --> " << sizeof(d) << endl;
    cout << "float --> " << sizeof(e) << endl;
    cout << "double --> " << sizeof(f) << endl;
}
